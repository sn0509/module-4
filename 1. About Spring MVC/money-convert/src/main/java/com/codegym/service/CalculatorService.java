package com.codegym.service;

public class CalculatorService {
    private double sum(int number1, int number2){
        return number1 + number2;
    }

    private double sub(int number1, int number2){
        return number1 - number2;
    }

    private double multi(int number1, int number2){
        return number1 * number2;
    }

    private double div(int number1, int number2){
        return number1 / number2;
    }
}
